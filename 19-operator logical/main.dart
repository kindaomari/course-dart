void main() {
//*logical AND  &&
  print('-----Logical AND------');
  print('true && flase =${true && false}');
  print('true && true =${true && true}');

//*logical OR  ||
  print('-----Logical OR------');
  print('true || flase =${1 | 0}');
  print('true && true =${true | true}');

  //*logical not  ||
  print('-----Logical NOT------');
  print('!true  =${!true}');
  print('!false  =${!false}');
}
