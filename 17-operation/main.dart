void main() {
//*addition
  int x = 5;
  int y = 10;
  print('$x+$y=${x + y}');

//*subtraction
  print('$x-$y=${x - y}');

//*multiply
  print('$x*$y=${x * y}');

//*division
  print('$x/$y=${x / y}');

  //*division int
  print('$x/$y=${x ~/ y}');

  //*modulus باقي القسمه
  print('$x%$y=${x % y}');
  int t = -3;
  //*unary minus
  print('$t=${-t}');
}
